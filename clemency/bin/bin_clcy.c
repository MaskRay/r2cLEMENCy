// [0x0000000, 0x4000000): Main Program Memory
static ut64 _baddr(RBinFile *arch) {
	return 0LL;
}

static bool _check_bytes(const ut8 *buf, ut64 len) {
	return len >= sizeof MAGIC && memcmp (buf, MAGIC, sizeof MAGIC) == 0;
}

static RBuffer *_create(RBin *_bin, const ut8 *code, int codelen, const ut8 *_data, int _datalen) {
	RBuffer *buf = r_buf_new ();
	if (buf) {
		r_buf_append_bytes (buf, code, codelen);
	}
	return buf;
}

static int _destroy(RBinFile *arch) {
	return true;
}

static RBinInfo *_info(RBinFile *arch) {
	RBinInfo *ret = R_NEW0 (RBinInfo);
	if (!ret) {
		return NULL;
	}
	ret->lang = "";
	ret->file = arch->file ? strdup (arch->file) : NULL;
	ret->type = NULL;
	ret->has_pi = 0; // no PIC/PIE
	ret->has_canary = 0;
	ret->bits = 64; // in accordance with r_anal_plugin_clcy, use 64-bit esil registers to emulate 54-bit pair registers
	ret->big_endian = 0; // used by r_asm_set_big_endian(). Actually middle-endian, but anyway we have custom assembler
	ret->has_va = 1; // Keep io.va = 1
	ret->has_nx = 1; // displayed in bin_info()
	ret->dbg_info = 0; // displayed in bin_info()
	return ret;
}

static bool _load(RBinFile *arch) {
	return true;
}

// Initialize the NFO section
static RList *_patch_relocs(RBin *b) {
	ut64 len = sizeof CLEMENCY_NFO * 8 / 9;
	ut16 *buf = malloc (len * 2);
	if (!buf) {
		return NULL;
	}
	ut64 x = 0, l = 0, n_buf = 0, i;
	for (i = 0; i < sizeof CLEMENCY_NFO; i++) {
		x = x << 8 | CLEMENCY_NFO[i];
		l += 8;
		if (l >= 9) {
			l -= 9;
			buf[n_buf++] = x >> l;
			x &= (1L << l) - 1;
		}
	}

	RIOSection sec = {.name = "NFO", .size = n_buf * 2, .vsize = 0x4000, .flags = R_IO_READ | R_IO_EXEC};
	(void)r_io_create_mem_map (b->iob.io, &sec, NFO_VADDR, false);
	(void)r_io_write_at (b->iob.io, NFO_VADDR, (const ut8 *)buf, len * 2);
	free (buf);
	return NULL;
}

static RList *_sections(RBinFile *arch) {
	static const struct {
		const char *name;
		ut64 vaddr;
		ut64 vsize;
		int srwx;
	} sections[] = {
		{"Main Program Memory", 0, 0x4000000, 5},
		{"Clock IO", 0x4000000, 0x1e, 6},
		{"Flag IO", 0x4010000, 0x1000, 4},
		{"Data Received", 0x5000000, 0x2000, 6},
		{"Data Received Size", 0x5002000, 0x2, 6},
		{"Data Sent", 0x5010000, 0x2000, 6},
		{"Data Sent Size", 0x5012000, 0x2, 6},
		//{"NFO", NFO_VADDR, 0x4000, 5}, // hidden section from clemency_nfo.c, created in _patch_relocs
		//{"Shared Memory", 0x6000000, 0x800000, 6},
		//{"NVRAM Memory", 0x6800000, 0x800000, 6},
		{"Interrupt Pointers", 0x7ffff00, 0x1c, 6},
		//{"Processor Identification and Features", 0x7ffff80, 0x80, 4},
	};
	RList *ret = r_list_newf (free);
	RBinSection *sec;
	ut64 sz = r_buf_size (arch->buf);
	int i;
	if (ret) {
		for (i = 0; i < R_ARRAY_SIZE (sections); i++) {
			if (i || sz) {
				if (!(sec = R_NEW0 (RBinSection))) {
					break;
				}
				sec->add = true;
				r_str_ncpy (sec->name, sections[i].name, sizeof sec->name);
				sec->paddr = 0;
				sec->size = i ? 0 : sz;
				sec->vaddr = sections[i].vaddr;
				sec->vsize = sections[i].vsize;
				sec->srwx = sections[i].srwx;
				if (!r_list_append (ret, sec)) {
					break;
				}
			}
		}
	}
	return ret;
}

